#!/usr/env/bin python3
# This file is run by update.sh and generates the file
# _data/security_patch_data.yml and the files in _security_patches_by_version/
# using the file _config.yml and the files in _security_patches/.

import glob
from pathlib import Path
import yaml

FILENAME = Path(__file__).name

# Start building a list of events that will be used for a sweep line algorithm
# for creating the files in _security_patches_by_version/. Each event is a
# tuple that has:
# 1. An Open XDMoD version tuple (X, Y, Z) for version X.Y.Z.
# 2. START to indicate the event is starting a new version range or END to
#    indicate it is ending the current version range.
# 3. VERSION if this event is associated with an Open XDMoD version range or
#    PATCH if it is associated with a security patch range.
# 4. The value of the Open XDMoD major version or security patch.
events = []
START = 0
END = 1
VERSION = 0
PATCH = 1

def get_version_tuple(version_string, split_char='.'):
    return tuple(map(int, version_string.split(split_char)))

# Get Open XDMoD version number ranges from _config.yml.
max_minor_versions = {}
with open('_config.yml', 'r') as f:
    site_config = yaml.safe_load(f)
for page_config in site_config['defaults']:
    if not page_config['scope']['type'] == 'pages':
        continue
    major_version = page_config['values']['version']
    min_minor_version = major_version + '.0'
    max_minor_versions[major_version] = page_config['values']['sw_version']
    events.append((
        get_version_tuple(min_minor_version),
        START,
        VERSION,
        major_version,
    ))
    events.append((
        get_version_tuple(page_config['values']['sw_version']),
        END,
        VERSION,
        major_version
    ))

# Get patch files from _security_patches/.
for patch_file in Path('_security_patches').glob('*.patch'):
    patch_file_name_parts = patch_file.name.rstrip('.patch').split('-')
    events.append((
        get_version_tuple(patch_file_name_parts[-2], split_char='_'),
        START,
        PATCH,
        patch_file.name,
    ))
    events.append((
        get_version_tuple(patch_file_name_parts[-1], split_char='_'),
        END,
        PATCH,
        patch_file.name,
    ))

# Sort the events by version number with ends happening before starts.
events.sort()

# Use the sweep line algorithm to determine which patches apply to which
# versions.
def get_version_from_tuple(version_tuple, separator):
    return separator.join([str(v) for v in version_tuple])

patches_by_major_version = {}
current_major_version = None
current_min_minor_version = None
current_patches = set()
previous_version = (0, 0, 0)
for version, start_or_end, version_or_patch, value in events:
    if (START, VERSION) == (start_or_end, version_or_patch):
        current_major_version = value
        current_min_minor_version = get_version_from_tuple(version, '_')
        patches_by_major_version[current_major_version] = {}
    elif (END, VERSION) == (start_or_end, version_or_patch):
        if current_patches:
            version_underscored = get_version_from_tuple(version, '_')
            if current_min_minor_version is None or version_underscored == current_min_minor_version:
                minor_versions = version_underscored
            else:
                minor_versions = current_min_minor_version + '-' + version_underscored
            patches_by_major_version[current_major_version][minor_versions] = sorted(list(current_patches))
        current_major_version = None
        current_min_minor_version = None
    elif (START, PATCH) == (start_or_end, version_or_patch):
        current_patches.add(value)
    elif (END, PATCH) == (start_or_end, version_or_patch):
        if current_major_version is not None and version != previous_version:
            minor_versions = current_min_minor_version + '-' + get_version_from_tuple(version, '_')
            patches_by_major_version[current_major_version][minor_versions] = sorted(list(current_patches))
            current_min_minor_version = None
        current_patches.remove(value)
    previous_version = version

# Generate the file _data/security_patch_data.yml and the files in _security_patches_by_version/.
upgrade_instructions_list = []
is_first_major_version = True
with open('_data/security_patch_data.yml', 'w') as security_patch_data_file:
    security_patch_data_file.write(f'# This file is generated by {FILENAME}\n')
    security_patch_data_file.write(f'all_linked_versions:\n')
    for version in site_config['version_list']:
        security_patch_data_file.write(f'  - "{ version }"\n')
    for version in site_config['unsupported_version_list']:
        security_patch_data_file.write(f'  - "{ version }"\n')
    overall_min_version = next((e for e in events if e[1] == START and e[2] == PATCH), None)[0]
    overall_max_version = next((e for e in reversed(events) if e[1] == END and e[2] == PATCH), None)[0]
    if overall_min_version == (0, 0, 0):
        versions_with_vulnerabilities = '≤' + get_version_from_tuple(overall_max_version, '.')
    else:
        versions_with_vulnerabilities = (
            get_version_from_tuple(overall_min_version, '.')
            + ' through '
            + get_version_from_tuple(overall_max_version, '.')
        )
    security_patch_data_file.write(f'versions_with_vulnerabilities: "{versions_with_vulnerabilities}"\n')
    security_patch_data_file.write(f'versions_with_group_by_note:\n')
    security_patch_data_file.write(f'  - "9.5"\n')
    security_patch_data_file.write(f'  - "9.0"\n')
    security_patch_data_file.write(f'  - "8.5"\n')
    for major_version in reversed(patches_by_major_version):
        security_patch_data_file.write(f'"{major_version}":\n')
        security_patch_data_file.write('  patch_files:\n')
        for filename in patches_by_major_version[major_version]:
            security_patch_data_file.write(f'    - "{filename}.patch"\n')
            versions = filename.split('-')
            if len(versions) == 1:
                versions_text = 'version ' + versions[0].replace('_', '.')
            else:
                versions_text = 'versions ' + versions[0].replace('_', '.') + ' through ' + versions[1].replace('_', '.')
            with open(f'_security_patches_by_version/{filename}.patch', 'w') as output_file:
                output_file.write(f'---\n')
                output_file.write(f'comment: This file was generated by {FILENAME}\n')
                output_file.write(f'---\n')
                output_file.write(f'# Workaround patch file for Open XDMoD {versions_text}\n')
                output_file.write(f'# Patches the following vulnerabilities:\n')
                line_start = '# - https://github.com/ubccr/xdmod/security/advisories/'
                output_file.write(line_start)
                ghsa_ids = ['-'.join(p.split('-')[:4]) for p in patches_by_major_version[major_version][filename]]
                output_file.write(f'\n{line_start}'.join(ghsa_ids) + '\n')
                for patch_filename in patches_by_major_version[major_version][filename]:
                    with open(f'_security_patches/{patch_filename}', 'r') as patch_file:
                        found_front_matter_start = False
                        found_front_matter_end = False
                        for line in patch_file:
                            if line.strip() == '---':
                                if found_front_matter_start:
                                    found_front_matter_end = True
                                else:
                                    found_front_matter_start = True
                            elif found_front_matter_end:
                                output_file.write(line)
        upgrade_instructions_list.append(
            f'<a href=\\"/{major_version}/upgrade.html\\">upgrade to version {max_minor_versions[major_version]}</a>'
        )
        if is_first_major_version:
            is_first_major_version = False
        else:
            l = list(reversed(upgrade_instructions_list))
            if max_minor_versions[major_version].split('.')[-1] == '0':
                del l[0]
            upgrade_instructions_text = ', then '.join(l)
            security_patch_data_file.write(f"  upgrade_instructions_text: \"{upgrade_instructions_text}\"\n")
